function t(key) {
    const lang = LANGUAGES[currentLang] || LANGUAGES.pt;
    return lang[key] || LANGUAGES.pt[key] || key;
}

function setLanguage(lang) {
    if (!LANGUAGES[lang]) return;
    currentLang = lang;
    localStorage.setItem('soplus_lang', lang);
    if (currentUser && userProfile) {
        updateUserProfile(currentUser.uid, { lang });
    }
}

function loadSavedLanguage() {
    const saved = localStorage.getItem('soplus_lang');
    if (saved && LANGUAGES[saved]) {
        currentLang = saved;
    } else if (userProfile?.lang && LANGUAGES[userProfile.lang]) {
        currentLang = userProfile.lang;
    }
}

function getLanguageList() {
    return Object.entries(LANGUAGES).map(([code, lang]) => ({
        code,
        name: lang._name,
        flag: lang._flag
    }));
}
